async function main() {
  const fire = await db.systemType.findUnique({ where: { code: 'fire' } });
  if (!fire) { console.error('Тип fire не найден'); process.exit(1); }
  console.log(`fire.calcEngine = ${fire.calcEngine} (должно быть skill — расчёт не затрагивается)`);

  const existing = await db.instruction.count({ where: { typeCode: 'fire' } });
  if (existing > 0) {
    console.log(`У fire уже есть инструкции (${existing}) — НЕ трогаю, чтобы не затереть правки.`);
    await db.$disconnect();
    return;
  }

  for (const s of SECTIONS) {
    await db.instruction.create({
      data: {
        typeCode: 'fire', section: s.section, status: 'active',
        items: { create: s.items.map((it, i) => ({ title: it.title, paramKey: it.paramKey ?? null, body: it.body, order: i })) },
      },
    });
    console.log(`  раздел ${s.section}: ${s.items.length} пунктов`);
  }
  const total = await db.instructionItem.count({ where: { instruction: { typeCode: 'fire' } } });
  console.log(`Готово: инструкции fire наполнены (${total} пунктов, все разделы active).`);
  await db.$disconnect();
}
main().catch((e) => { console.error(e); process.exit(1); });
