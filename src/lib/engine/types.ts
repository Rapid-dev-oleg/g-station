/**
 * Контракт модуля типа станции — код-эквивалент `типы/README.md`
 * (6 фиксированных разделов контракта типа).
 *
 * Конвейер из 5 шагов одинаков для всех типов; специфика типа — в модуле,
 * реализующем `TypeModule`. Шаги обращаются к модулю по номеру раздела.
 */

import type {
  Equipment,
  Output,
  Station,
  StationCalc,
  StationInput,
  StationType,
  Variant,
} from '@/lib/dossier/types';

/** Балл соответствия типа входным данным (для диспетчера шага 1). */
export type MatchScore = number;

/**
 * Модуль типа станции. Каждый метод соответствует разделу контракта типа.
 */
export interface TypeModule {
  /** Идентификатор типа. */
  id: StationType;

  /** Человекочитаемое имя типа. */
  label: string;

  // ─── Раздел 1 — Идентификация (диспетчер шага 1) ──────────────────────
  /**
   * Балл соответствия входных данных этому типу.
   * Диспетчер выбирает модуль с максимальным баллом.
   */
  matchTriggers(input: StationInput): MatchScore;

  // ─── Раздел 2 — Нормативная база ──────────────────────────────────────
  /** Применимые своды правил / ГОСТ. */
  norms: string[];

  // ─── Раздел 3 — Опросный лист (полнота шага 1) ────────────────────────
  /**
   * Обязательные поля карточки для данного типа и сценария.
   * Пути полей в `StationInput` (точечная нотация).
   */
  requiredFields(input: StationInput): string[];

  // ─── Раздел 4 — Нормативный расчёт (шаг 2) ────────────────────────────
  /**
   * Нормативный расчёт, специфичный для типа (объём пожарного запаса и т.п.).
   * Возвращает фрагмент `StationCalc` для слияния.
   */
  computeNormative(station: Station): Partial<StationCalc>;

  // ─── Раздел 5 — Особенности подбора оборудования (шаг 3) ──────────────
  /**
   * Спец-номенклатура типа: серия ШУ, материал коллектора, спецоборудование.
   * Дополняет уже собранный движком `Equipment`.
   */
  selectEquipment(variant: Variant, calc: StationCalc, input: StationInput): Equipment;

  // ─── Раздел 6 — Особенности оформления (шаг 5) ────────────────────────
  /** Состав документов ТП для данного типа. */
  documentSpec(): { sections: string[] };

  /** Сегменты шифра изделия (серия, назначение, опции). */
  codeSegments(station: Station, variant: Variant): NonNullable<Output['code_segments']>;
}
